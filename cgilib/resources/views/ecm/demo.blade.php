<h3 class="about_tit01">ご挨拶</h3>

<div class="about_01">
<p class="font16 f_hm mb15">当社は昭和37年創業以来、</p>
<p class="font16 f_hm mb15"><span class="font32 f_hm org">日本トップクラス</span>の水質を誇る</p>
<p class="font16 f_hm mb15">倶多楽湖のふもと白老は虎杖浜に、水産加工一筋に皆様の食卓に</p>
<p class="font16 f_hm mb25"><span class="font32 f_hm org">日本伝統の食文化</span>のご提供をさせていただいております。</p>
<p class="mb10">これからも良質な味の提供と徹底した品質管理を第一に考え、皆様方のご期待に添えるよう努力を惜しまず、日本の食文化の向上のため当社はまい進していきます。</p>
<p>きれいな海、自然豊かな北海道の空気のもと、北海道虎杖浜の美味しい水で加工した<span class="org">「竹丸渋谷水産株式会社」</span>の水産加工品を今後ともよろしくお願いいたします。</p>
